**Minutes 3-7: Breath Awareness**
Bring attention to natural breath. Count "one" on inhale, "two" on exhale, up to ten. When mind wanders, gently return to one.

**Minutes 8-9: Body Scan**
Briefly scan from head to toes, releasing tension. No need to change anything, just observe.

**Minute 10: Gratitude**
Reflect on one thing you''re grateful for. Slowly open eyes.

